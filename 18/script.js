let visited = new Set([])
let open = new Set([state])
let running = true
let steps = 0
while(running) {
    let newOpen = new Set([])
    for(let state of open) {
        visited.add(state);
        if(state.split("|")[1].length === keys.length) {
            drawField(state)
            console.log("found solution in", steps, state.split("|")[1])
            running = false
            break
        }
        
        // Check neighbours
        for(let neighbour of findNeighbours(state)) {
            if(visited.has(neighbour)) {
                continue;
            }
            newOpen.add(neighbour);
        }
    }
    
    console.log('Steps', steps, open.length, newOpen.length)
    open = newOpen
    steps++
}
